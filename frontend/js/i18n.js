const I18nStore = {
  KEY: "aeris_lang",
  get() { return localStorage.getItem(this.KEY) || "en"; },
  set(lang) { localStorage.setItem(this.KEY, lang); },
};

/* translate a single key for current language */
function t(key, lang) {
  const l = lang || I18nStore.get();
  return (I18N[l] && I18N[l][key]) || (I18N.en[key]) || key;
}

/* apply translations to the whole document */
function applyI18n(lang) {
  const l = lang || I18nStore.get();
  document.documentElement.lang = l;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (!key) return;                 // empty key = leave content as-is
    el.textContent = t(key, l);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"), l);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph"), l));
  });
  document.querySelectorAll("[data-i18n-aria]").forEach(el => {
    el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria"), l));
  });

  // sync language switch buttons
  document.querySelectorAll(".lang-switch button").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === l);
  });
}

/* Switch language: persist, translate DOM, then notify pages once
   so they can re-render dynamic content. Kept separate from
   applyI18n() so re-renders don't loop. */
function setLanguage(lang) {
  I18nStore.set(lang);
  applyI18n(lang);
  document.dispatchEvent(new CustomEvent("languagechange", { detail: { lang } }));
}
